import string

print("–ü—Ä–æ–≥—Ä–∞–º–º–∞ –≤—ã–≤–æ–¥–∏—Ç —Ñ—Ä–∞–≥–º–µ–Ω—Ç —Ç–∞–±–ª–∏—Ü—ã —É–º–Ω–æ–∂–µ–Ω–∏—è. –û—Ç a –¥–æ b, —É–º–Ω–æ–∂–µ–Ω–Ω—ã—Ö –Ω–∞ —á–∏—Å–ª–∞ –æ—Ç c –¥–æ d.")
a, b, c, d = map(int, input('–í–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–∞ "a","b","c","d" —á–µ—Ä–µ–∑ –∑–∞–ø—è—Ç—É—é: ').split(','))
try:
    if (a, b, c, d < 10) or (a <= b) or (c <= d):
        print('', end='\t')
        for i in range(c, d + 1):
            print(i, end='\t')
        print('')
        for j in range(a, b + 1):
            print(j, end='\t')
            for i in range(c, d + 1):
                print(i * j, end='\t')
            print('')
except Exception:
    print('–í–≤–µ–¥–µ–Ω—ã –Ω–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–µ —Å–∏–º–≤–æ–ª—ã!')
